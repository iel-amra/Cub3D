#include <unistd.h>

int	ft_putendl(char *s)
{
	int	n;

	n = 0;
	if (!s)
		return (n);
	while (*s)
	{
		n += write(1, s, 1);
		s++;
	}
	n += write(1, "\n", 1);
	return (n);
}
